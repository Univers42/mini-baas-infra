#!/bin/bash

################################################################################
# log_hook.sh
# Logging utility for Git hooks with colored output.
# Provides functions for consistent logging across all hooks.
################################################################################

# Color codes
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

################################################################################
# log_info: Print informational message (blue)
################################################################################
log_info() {
  printf "${BLUE}ℹ️  INFO${NC}: %s\n" "$1" >&2
}

################################################################################
# log_success: Print success message (green)
################################################################################
log_success() {
  printf "${GREEN}✅ SUCCESS${NC}: %s\n" "$1" >&2
}

################################################################################
# log_error: Print error message (red)
################################################################################
log_error() {
  printf "${RED}❌ ERROR${NC}: %s\n" "$1" >&2
}

################################################################################
# log_warn: Print warning message (yellow)
################################################################################
log_warn() {
  printf "${YELLOW}⚠️  WARN${NC}: %s\n" "$1" >&2
}

################################################################################
# log_section: Print section header (blue, bold)
################################################################################
log_section() {
  printf "\n${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}\n" >&2
  printf "${BLUE}→ %s${NC}\n" "$1" >&2
  printf "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}\n" >&2
}

################################################################################
# log_debug: Print debug message (only if DEBUG=1)
################################################################################
log_debug() {
  if [[ "${DEBUG:-0}" == "1" ]]; then
    printf "${YELLOW}🐛 DEBUG${NC}: %s\n" "$1" >&2
  fi
}
